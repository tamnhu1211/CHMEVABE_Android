package com.example.connect.model;

public class loaisp {
    int id;
    String tênsp;
    String hìnhảnh;
    String thôngtinsp;
    int giá;
    String nhàsảnxuất;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTênsp() {
        return tênsp;
    }

    public void setTênsp(String tênsp) {
        this.tênsp = tênsp;
    }

    public String getHìnhảnh() {
        return hìnhảnh;
    }

    public void setHìnhảnh(String hìnhảnh) {
        this.hìnhảnh = hìnhảnh;
    }

    public String getThôngtinsp() {
        return thôngtinsp;
    }

    public void setThôngtinsp(String thôngtinsp) {
        this.thôngtinsp = thôngtinsp;
    }

    public int getGiá() {
        return giá;
    }

    public void setGiá(int giá) {
        this.giá = giá;
    }

    public String getNhàsảnxuất() {
        return nhàsảnxuất;
    }

    public void setNhàsảnxuất(String nhàsảnxuất) {
        this.nhàsảnxuất = nhàsảnxuất;
    }
}
